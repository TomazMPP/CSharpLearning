class Titular
{
    public Titular(string nomeDoTitular, string cpfDoTitular, string ruaDoTitular)
    {
        NomeDoTitular = nomeDoTitular;
        CpfDoTitular = cpfDoTitular;
        RuaDoTitular = ruaDoTitular;
    }

    public string NomeDoTitular { get; }
    public string CpfDoTitular { get; }
    public string RuaDoTitular { get; }

}